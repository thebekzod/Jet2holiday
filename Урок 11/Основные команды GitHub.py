# Основные команды GIT

# git init                      — создать новый репозиторий
# git status                    — проверить состояние (какие файлы изменены, не добавлены и т.д.)
# git add <файл>                — добавить файл к коммиту
# git add .                     — добавить все изменения
# git commit -m "сообщение"     — зафиксировать изменения
# git log --oneline             — посмотреть историю коммитов
# git branch                    — посмотреть ветки
# git branch -M main            — переименовать ветку в main
# git remote add origin <url>   — добавить ссылку на GitHub
# git remote -v                 — проверить подключение
# git push -u origin main       — отправить первый раз
# git push                      — отправить изменения
# git pull                      — скачать обновления с GitHub
# git clone <url>               — скопировать чужой репозиторий
